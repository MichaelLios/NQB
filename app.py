# ── Dollar conversion ──────────────────────────────────────────────────────────
# 1R = base risk per trade in dollars
BASE_RISK_USD = min(
    cfg.DEFAULT_ACCOUNT_SIZE * cfg.DEFAULT_RISK_PCT / 100,
    cfg.DEFAULT_MAX_LOSS
)

def _usd(r_val, signed=True):
    """Convert R multiple to formatted dollar string."""
    if r_val is None:
        return "—"
    usd = r_val * BASE_RISK_USD
    if signed:
        sign = "+" if usd >= 0 else "−"
        return f"{sign}${abs(usd):,.0f}"
    return f"${abs(usd):,.0f}"

def _usd_val(r_val):
    """Raw dollar value from R."""
    if r_val is None:
        return 0.0
    return r_val * BASE_RISK_USD


