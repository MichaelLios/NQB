def _build_chart(df: pd.DataFrame, sig, tf_key: str) -> go.Figure:
    display_df = df.iloc[-120:].copy()
    xs = display_df.index

    fig = make_subplots(rows=2, cols=1, shared_xaxes=True,
                        row_heights=[0.82, 0.18], vertical_spacing=0.01)

    fig.add_trace(go.Candlestick(
        x=xs, open=display_df["open"], high=display_df["high"],
        low=display_df["low"], close=display_df["close"],
        increasing_line_color=BULL, decreasing_line_color=BEAR,
        increasing_fillcolor=BULL, decreasing_fillcolor=BEAR,
        name="NQ", line=dict(width=1),
    ), row=1, col=1)

    bar_colors = [
        f"rgba(52,211,153,{0.5 if c>=o else 0.15})" if c>=o
        else f"rgba(251,113,133,{0.5 if c<o else 0.15})"
        for c, o in zip(display_df["close"], display_df["open"])
    ]
    fig.add_trace(go.Bar(x=xs, y=display_df["volume"],
                         marker_color=bar_colors, showlegend=False), row=2, col=1)

    sh, sl = liq.swing_points(display_df, left=5, right=5)
    if not sh.empty:
        fig.add_trace(go.Scatter(x=sh.index, y=sh["level"] * 1.00025, mode="markers",
                                  marker=dict(symbol="triangle-down", color=BEAR, size=7, opacity=.8),
                                  showlegend=False, hovertemplate="SH %{y:,.1f}<extra></extra>"),
                      row=1, col=1)
    if not sl.empty:
        fig.add_trace(go.Scatter(x=sl.index, y=sl["level"] * 0.99975, mode="markers",
                                  marker=dict(symbol="triangle-up", color=BULL, size=7, opacity=.8),
                                  showlegend=False, hovertemplate="SL %{y:,.1f}<extra></extra>"),
                      row=1, col=1)

    eq_lvls = liq.equal_levels(sh, sl)
    eq_near = sorted(eq_lvls, key=lambda e: abs(e["level"] - sig.price))[:6]
    for eq in eq_near:
        is_high = eq["kind"] == "EQH"
        color   = "rgba(251,113,133,.45)" if is_high else "rgba(52,211,153,.45)"
        fig.add_hline(y=eq["level"], line=dict(color=color, width=1, dash="dot"),
                       annotation_text=eq["kind"],
                       annotation_font_color=BEAR if is_high else BULL,
                       annotation_font_size=9, annotation_xanchor="left", row=1, col=1)

    for ob in liq.order_blocks(display_df):
        is_bull = ob["kind"] == "bull_ob"
        fig.add_hrect(
            y0=ob["bottom"], y1=ob["top"],
            fillcolor="rgba(52,211,153,.08)" if is_bull else "rgba(251,113,133,.08)",
            line=dict(color="rgba(52,211,153,.4)" if is_bull else "rgba(251,113,133,.4)", width=1),
            annotation_text="OB ▲" if is_bull else "OB ▼",
            annotation_font_color=BULL if is_bull else BEAR,
            annotation_font_size=9, annotation_xanchor="left", row=1, col=1,
        )

    vp = liq.volume_profile(display_df, bins=36)
    max_vol = vp["volume"].max()
    p_range = display_df["high"].max() - display_df["low"].min()
    bin_half = p_range / (2 * len(vp))
    hvn_q = vp["volume"].quantile(0.80)
    if max_vol > 0:
        for _, row_vp in vp.iterrows():
            norm = row_vp["volume"] / max_vol
            fig.add_shape(
                type="rect", xref="paper", yref="y1",
                x0=1.0 - norm * 0.07, x1=1.0,
                y0=row_vp["price_mid"] - bin_half, y1=row_vp["price_mid"] + bin_half,
                fillcolor="rgba(52,211,153,.45)" if row_vp["volume"] >= hvn_q else "rgba(40,48,60,.5)",
                line_width=0,
            )

    if sig.stop_loss and sig.target and "NEUTRAL" not in sig.signal:
        for y_val, color, label in [
            (sig.price,     INFO,  f"Entry  {sig.price:,.1f}"),
            (sig.stop_loss, BEAR,  f"Stop  {sig.stop_loss:,.1f}"),
            (sig.target,    BULL,  f"Target  {sig.target:,.1f}"),
        ]:
            fig.add_hline(y=y_val,
                           line=dict(color=color, width=1.5,
                                     dash="solid" if y_val == sig.price else "dash"),
                           annotation_text=label,
                           annotation_font_color=color, annotation_font_size=10,
                           annotation_xanchor="right", row=1, col=1)
        if sig.partial_tp1 > 0:
            fig.add_hline(y=sig.partial_tp1,
                           line=dict(color=WARN, width=1, dash="dot"),
                           annotation_text=f"TP1  {sig.partial_tp1:,.1f}",
                           annotation_font_color=WARN, annotation_font_size=9,
                           annotation_xanchor="right", row=1, col=1)
        fig.add_hrect(y0=min(sig.price, sig.stop_loss), y1=max(sig.price, sig.stop_loss),
                       fillcolor="rgba(251,113,133,.06)", line_width=0, row=1, col=1)
        fig.add_hrect(y0=min(sig.price, sig.target), y1=max(sig.price, sig.target),
                       fillcolor="rgba(52,211,153,.06)", line_width=0, row=1, col=1)

    axis_c = dict(gridcolor="rgba(30,41,59,.8)", zerolinecolor="rgba(30,41,59,.8)",
                  tickfont=dict(size=10, color=DIM), showline=False, zeroline=False)
