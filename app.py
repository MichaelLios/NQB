def _exp_table(rows, key):
    if not rows:
        return html.Span("No data yet.", style={"color": MUTED, "fontSize": "12px"})
    # Convert R to $ for display
    disp = []
    for r in rows:
        d = dict(r)
        d["exp_$"]      = _usd(r.get("expectancy"))
        d["avg_win_$"]  = _usd(r.get("avg_win_r"),  signed=False)
        d["avg_loss_$"] = _usd(r.get("avg_loss_r"), signed=False)
        disp.append(d)
    return dash_table.DataTable(
        data=disp,
        columns=[{"name": c.replace("_"," ").upper(), "id": c}
                 for c in [key, "trades", "win_rate", "avg_win_$", "avg_loss_$", "exp_$"]],
        style_table={"overflowX": "auto"},
        style_header={"backgroundColor": BG_2, "color": MUTED, "fontWeight": "600",
                       "fontSize": "10px", "border": f"1px solid {LINE}", "textTransform": "uppercase"},
        style_cell={"backgroundColor": BG_1, "color": TEXT, "fontSize": "11px",
                    "border": f"1px solid {LINE}", "padding": "4px 8px"},
        style_data_conditional=[
            {"if": {"filter_query": '{exp_$} contains "+"'}, "color": BULL},
            {"if": {"filter_query": '{exp_$} contains "−"'}, "color": BEAR},
        ],
        sort_action="native", page_size=8,
    )


