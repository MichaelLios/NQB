@app.callback(
    Output("tb-clock", "children"),
    Output("ss-now",   "style"),
    Input("clock-timer", "n_intervals"),
)
def tick_clock(_):
    now = datetime.now(NY)
    clock = now.strftime("%H:%M:%S")
    h = now.hour + now.minute / 60
    left = max(0.0, min(100.0, (h - 4.0) / STRIP_SPAN * 100))
    style = {"left": f"{left:.1f}%", "position": "absolute", "top": "0",
             "bottom": "0", "width": "2px", "background": "white",
             "opacity": ".6", "transform": "translateX(-50%)"}
    return clock, style


