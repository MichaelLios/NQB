def refresh_profit(_, mode):
    all_trades = db_mod.get_completed_trades(paper_only=False)
    if mode == "live":
        trades = [t for t in all_trades if not t.get("paper_trade")]
    elif mode == "paper":
        trades = [t for t in all_trades if t.get("paper_trade")]
    else:
        trades = all_trades

    stats   = ana_mod.overall_stats(trades)
    no_data = html.Span("No completed trades yet.", style={"color": MUTED, "fontSize": "13px"})

    if not trades or not stats:
        empty = [no_data]
        return empty, no_data, no_data, no_data, no_data, no_data, no_data, no_data, no_data, no_data

    net_usd  = _usd_val(stats.get("net_r"))
    exp_usd  = _usd_val(stats.get("expectancy"))
    dd_usd   = _usd_val(stats.get("max_drawdown_r", 0))
    avg_usd  = _usd_val(stats.get("avg_r_per_trade", 0))
    pf       = stats.get("profit_factor")
    wr       = stats.get("win_rate", 0)

    summary_row = [
        _kpi("Win Rate",     f"{wr:.1f}%",             tone="bull" if wr>=50 else "bear", big=True),
        _kpi("Net P&L",      _usd(stats.get("net_r")), tone="bull" if net_usd>=0 else "bear", big=True),
        _kpi("Profit Factor",str(pf) if pf else "—",   tone="bull" if (pf or 0)>=1 else "bear"),
        _kpi("Expectancy",   _usd(stats.get("expectancy")) if stats.get("expectancy") is not None else "—",
             sub="per trade", tone="bull" if exp_usd>=0 else "bear"),
        _kpi("Avg P&L/Trade", _usd(stats.get("avg_r_per_trade",0)),
             tone="bull" if avg_usd>=0 else "bear"),
        _kpi("Max Drawdown", _usd(stats.get("max_drawdown_r",0), signed=False),
             sub="peak-to-trough", tone="bear"),
        _kpi("Total Trades", str(stats.get("total",0))),
    ]

    sess_rows = ana_mod.win_rate_table("session", trades)

    def _mini(rows, key, desc=True):
        if not rows: return html.Span("Not enough data.", style={"color": MUTED, "fontSize": "12px"})
        display = sorted(rows, key=lambda r: r["win_rate"], reverse=desc)[:5]
        return dash_table.DataTable(
            data=display,
            columns=[{"name": c.replace("_"," ").upper(), "id": c}
                     for c in [key, "trades", "win_rate", "avg_rr"]],
            style_table={"overflowX": "auto"},
            style_header={"backgroundColor": BG_2, "color": MUTED, "fontWeight": "600",
                           "fontSize": "10px", "border": f"1px solid {LINE}", "textTransform": "uppercase"},
            style_cell={"backgroundColor": BG_1, "color": TEXT, "fontSize": "11px",
                        "border": f"1px solid {LINE}", "padding": "4px 8px",
                        "maxWidth": "220px", "overflow": "hidden", "textOverflow": "ellipsis"},
            style_data_conditional=[
                {"if": {"filter_query": "{win_rate} >= 55"}, "color": BULL},
                {"if": {"filter_query": "{win_rate} < 45"},  "color": BEAR},
            ],
            page_size=5,
        )

    best_sess  = _mini(sess_rows, "session", desc=True)
    worst_sess = _mini(sess_rows, "session", desc=False)

    def _setup_mini(rows, col):
        if not rows: return html.Span("Not enough data.", style={"color": MUTED, "fontSize": "12px"})
        return dash_table.DataTable(
            data=rows,
            columns=[{"name": c.upper(), "id": c} for c in ["setup","trades","win_rate","avg_r"]],
            style_table={"overflowX": "auto"},
            style_header={"backgroundColor": BG_2, "color": MUTED, "fontWeight": "600",
                           "fontSize": "10px", "border": f"1px solid {LINE}", "textTransform": "uppercase"},
            style_cell={"backgroundColor": BG_1, "color": TEXT, "fontSize": "11px",
                        "border": f"1px solid {LINE}", "padding": "4px 8px",
                        "maxWidth": "280px", "overflow": "hidden", "textOverflow": "ellipsis"},
            style_data_conditional=[{"if": {"column_id": "win_rate"}, "color": col}],
            page_size=5,
        )

    best_setup  = _setup_mini(ana_mod.best_setups(trades,  5), BULL)
    worst_setup = _setup_mini(ana_mod.worst_setups(trades, 5), BEAR)

    exp_sess  = _exp_table(ana_mod.expectancy_by("session",     trades), "session")
    exp_tf    = _exp_table(ana_mod.expectancy_by("timeframe",   trades), "timeframe")
    exp_grade = _exp_table(ana_mod.expectancy_by("grade",       trades), "grade")
    exp_stype = _exp_table(ana_mod.expectancy_by("signal_type", trades), "signal_type")

    recs = []
    if sess_rows:
        ws = min(sess_rows, key=lambda r: r["win_rate"])
        bs = max(sess_rows, key=lambda r: r["win_rate"])
        if ws["win_rate"] < 40:
            recs.append(f"Avoid {ws['session']} — {ws['win_rate']}% WR ({ws['trades']} trades). Consider filtering it out.")
        if bs["win_rate"] >= 60:
            recs.append(f"Focus on {bs['session']} — strongest at {bs['win_rate']}% WR.")
    for row in ana_mod.expectancy_by("grade", trades):
        exp_v = row.get("expectancy", 0)
        if exp_v < 0:
            recs.append(f"Grade {row['grade']} has NEGATIVE expectancy ({_usd(exp_v)}/trade) — raise filters or disable.")
        elif exp_v > 0.3:
            recs.append(f"Grade {row['grade']} is your best edge ({_usd(exp_v)}/trade) — consider larger size.")
    if (stats.get("avg_mae_pts",0) or 0) > (stats.get("avg_mfe_pts",0) or 0):
        recs.append("Avg MAE > Avg MFE — stops may be too tight or entries are chasing. Wait for deeper pullbacks.")
    if (pf or 0) < 1.0 and stats.get("total",0) >= 10:
        recs.append("Profit factor < 1.0 — losing more than winning in dollar terms. Focus on R:R ≥ 2.5 only.")
    if not recs:
        recs.append("Accumulate more completed trades for statistically meaningful recommendations.")

    rec_el = html.Div([
        html.Div(f"→  {r}", style={
            "color": WARN, "fontSize": "12px", "lineHeight": "2.0",
            "borderBottom": f"1px solid {LINE}", "padding": "4px 0",
        }) for r in recs
    ], style={"backgroundColor": BG_1, "border": f"1px solid {LINE}",
               "borderRadius": "6px", "padding": "12px 16px"})

    return (summary_row, best_sess, worst_sess, best_setup, worst_setup,
            exp_sess, exp_tf, exp_grade, exp_stype, rec_el)


