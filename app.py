@app.callback(
    Output("rc-results", "children"),
    [Input("rc-account",       "value"),
     Input("rc-risk-pct",      "value"),
     Input("rc-max-loss",      "value"),
     Input("rc-entry",         "value"),
     Input("rc-stop",          "value"),
     Input("rc-contract-type", "value")],
)
def compute_risk(account, risk_pct, max_loss, entry, stop, contract_type):
    try:
        account   = float(account  or cfg.DEFAULT_ACCOUNT_SIZE)
        risk_pct  = float(risk_pct or cfg.DEFAULT_RISK_PCT)
        max_loss  = float(max_loss or cfg.DEFAULT_MAX_LOSS)
        entry     = float(entry or 0)
        stop      = float(stop  or 0)
    except (TypeError, ValueError):
        return html.Span("Enter valid numbers", style={"color": MUTED})

    pt_val       = cfg.NQ_POINT_VALUE if contract_type == "NQ" else cfg.MNQ_POINT_VALUE
    risk_from_pct= account * risk_pct / 100
    dollar_risk  = min(risk_from_pct, max_loss)

    stop_pts = contracts = actual_risk = profit_25r = profit_3r = 0
    if entry > 0 and stop > 0 and entry != stop:
        stop_pts     = abs(entry - stop)
        dollar_per_c = stop_pts * pt_val
        contracts    = max(1, int(dollar_risk / dollar_per_c)) if dollar_per_c > 0 else 0
        actual_risk  = contracts * dollar_per_c
        profit_25r   = actual_risk * 2.5
        profit_3r    = actual_risk * 3.0

    def _row(label, val, color=TEXT2):
        return html.Div(style={"display": "flex", "justifyContent": "space-between",
                                "padding": "5px 0", "borderBottom": f"1px solid {LINE}"}, children=[
            html.Span(label, style={"color": MUTED, "fontSize": "12px"}),
            html.Span(val,   style={"color": color, "fontSize": "13px",
                                    "fontFamily": "ui-monospace,monospace", "fontWeight": "600"}),
        ])

    return html.Div(style={"backgroundColor": BG_2, "border": f"1px solid {LINE}",
                            "borderRadius": "6px", "padding": "14px 16px"}, children=[
        _row("Max $ Risk",           f"${dollar_risk:,.0f}",    WARN),
        _row("Stop Distance",        f"{stop_pts:.2f} pts"  if stop_pts else "—", DIM),
        _row(f"Contracts ({contract_type})", str(contracts) if contracts else "—",
             BULL if contracts else DIM),
        _row("Actual $ Risk",        f"${actual_risk:,.0f}" if actual_risk else "—", BEAR),
        _row("2.5R Target Profit",   f"${profit_25r:,.0f}"  if profit_25r else "—", BULL),
        _row("3R Target Profit",     f"${profit_3r:,.0f}"   if profit_3r  else "—", BULL),
    ])


