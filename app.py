STRIP_SESSIONS = [
    ("Pre-Market",  4.0,  9.5,  "neutral"),
    ("NY Open",     9.5,  11.0, "bull"),
    ("Lunch Chop", 11.0,  14.0, "warn"),
    ("Power Hour", 15.0,  16.0, "info"),
    ("After Hours",16.0,  18.0, "neutral"),
]
STRIP_SPAN = 18.0 - 4.0   # 14 hours

def _session_strip():
    blocks = []
    for name, start, end, tone in STRIP_SESSIONS:
        left  = (start - 4.0) / STRIP_SPAN * 100
        width = (end - start) / STRIP_SPAN * 100
        blocks.append(html.Div(
            name, className=f"nqb-ss-block {tone}",
            style={"left": f"{left:.1f}%", "width": f"{width:.1f}%"},
        ))
    # Current time marker (static on load; updates via id="ss-now")
    blocks.append(html.Div(id="ss-now", className="nqb-ss-now"))

    axis_labels = [html.Span(f"{h:02d}:00", className="mono dim") for h in [4, 7, 10, 13, 16, 18]]

    return html.Div(className="nqb-session-strip", children=[
        html.Div(blocks, className="nqb-ss-track"),
        html.Div(axis_labels, className="nqb-ss-axis"),
    ])


