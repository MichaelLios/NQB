@app.callback(
    [Output("tb-price",       "children"),
     Output("tb-delta",       "className"),
     Output("tb-delta",       "children"),
     Output("tb-session",     "children"),
     Output("kpi-row",        "children"),
     Output("mtf-panel",      "children"),
     Output("context-panel",  "children"),
     Output("positions-panel","children"),
     Output("main-chart",     "figure"),
     Output("news-warning",   "children"),
     Output("last-alert-key", "data")],
    [Input("active-tf",     "data"),
     Input("refresh-timer", "n_intervals")],
    State("last-alert-key", "data"),
)
def refresh(tf_key, _, last_alert_key):
    tf_key = tf_key or "swing"
    tf = cfg.TIMEFRAMES[tf_key]

    df = datamod.fetch(interval=tf["interval"], period=tf["period"])
    if df is None or df.empty:
        return ("—", "nqb-ticker-delta", "", _session_pill_el(),
                [], _empty_panel("No data"), _empty_panel(""), _empty_panel(""),
                _empty_fig("No data — market may be closed"), "", last_alert_key)

    df  = indicators.add_all(df)
    sig = sig_engine.evaluate(df, tf["label"])

    # HTF confirmation
    htf_key = tf.get("htf")
    htf_sig = None
    htf_df  = None
    if htf_key:
        htf_tf = cfg.TIMEFRAMES[htf_key]
        htf_df = datamod.fetch(interval=htf_tf["interval"], period=htf_tf["period"])
        if htf_df is not None and not htf_df.empty:
            htf_df  = indicators.add_all(htf_df)
            htf_sig = sig_engine.evaluate(htf_df, htf_tf["label"])

    sig_engine.add_grade(sig, htf_sig)

    # Market context + filter
    htf_direction = "NEUTRAL"
    if htf_sig:
        if "BUY"  in htf_sig.signal and "NEUTRAL" not in htf_sig.signal: htf_direction = "BULL"
        elif "SELL" in htf_sig.signal and "NEUTRAL" not in htf_sig.signal: htf_direction = "BEAR"

    try:
        context = mc_mod.classify(df, htf_direction)
    except Exception:
        context = None

    try:
        filter_result = flt_mod.evaluate(sig, df, context)
    except Exception:
        filter_result = None

    if filter_result and filter_result.allowed and sig.grade in ("A", "A+"):
        try:
            sig = sig_engine.enhance_signal(sig, df)
        except Exception:
            pass

    # Track open outcomes
    sess = sess_mod.current_session()
    preloaded = {tf_key: df}
    if htf_key and htf_df is not None:
        preloaded[htf_key] = htf_df
    try:
        trk_mod.check_all_open(preloaded=preloaded)
    except Exception:
        pass

    # Log + alert
    alert_key = last_alert_key
    is_tradeable = (sig.grade in ("A", "A+") and "NEUTRAL" not in sig.signal
                    and sig.stop_loss and (filter_result is None or filter_result.allowed))
    if is_tradeable:
        new_key = f"{sig.signal}|{sig.grade}|{tf_key}|{round(sig.price, -1)}"
        if new_key != last_alert_key:
            try: db_mod.log_signal(sig, session=sess["name"], paper=False)
            except Exception: pass
            journal_mod.log_signal(sig)
            if pt_mod.is_enabled():
                try: pt_mod.open_trade(sig, session=sess["name"])
                except Exception: pass
            alert_mod.send_if_notable(sig)
            alert_key = new_key

    # ── Price / change ─────────────────────────────────────────────────────
    price      = sig.price
    prev_close = float(df["close"].iloc[-2]) if len(df) > 1 else price
    chg        = price - prev_close
    chg_pct    = chg / prev_close * 100 if prev_close else 0
    delta_cls  = "nqb-ticker-delta up" if chg >= 0 else "nqb-ticker-delta down"
    delta_txt  = f"{'▲' if chg >= 0 else '▼'} {abs(chg):,.1f}  ({abs(chg_pct):.2f}%)"

    # ── Session pill ───────────────────────────────────────────────────────
    sess_el = _session_pill_el(sess)

    # ── KPI row ────────────────────────────────────────────────────────────
    try:
        trades = db_mod.get_completed_trades()
        stats  = ana_mod.overall_stats(trades)
    except Exception:
        stats  = {}

    net_usd = _usd_val(stats.get("net_r"))
    exp_usd = _usd_val(stats.get("expectancy"))
    dd_usd  = _usd_val(stats.get("max_drawdown_r", 0))
    wr      = stats.get("win_rate", 0)

    kpi_items = [
        _kpi("Net P&L",
             _usd(stats.get("net_r")) if stats else "—",
             sub=f"{stats.get('total',0)} completed trades",
             tone="bull" if net_usd >= 0 else "bear", big=True),
        _kpi("Win Rate",
             f"{wr:.1f}%" if stats else "—",
             sub=f"{stats.get('wins',0)}W / {stats.get('losses',0)}L",
             tone="bull" if wr >= 50 else "bear"),
        _kpi("Profit Factor",
             str(stats.get("profit_factor") or "—"),
             sub="gross profit / gross loss",
             tone="bull" if (stats.get("profit_factor") or 0) >= 1 else "bear"),
        _kpi("Expectancy",
             _usd(stats.get("expectancy")) if stats else "—",
             sub="per trade · 1R = $" + f"{BASE_RISK_USD:,.0f}",
             tone="bull" if exp_usd >= 0 else "bear"),
        _kpi("Max Drawdown",
             _usd(stats.get("max_drawdown_r", 0), signed=False) if stats else "—",
             sub="peak-to-trough", tone="bear"),
        _kpi("Confluence",
             f"{max(sig.bull_score, sig.bear_score):.0f}/100",
             sub=f"{'BUY' if 'BUY' in sig.signal else 'SELL' if 'SELL' in sig.signal else 'NEUTRAL'} · {tf['label']}"),
    ]

    # ── MTF signal panel ───────────────────────────────────────────────────
    mtf_panel_el = _build_mtf_panel(sig, htf_sig, htf_key, tf_key, filter_result, context)

    # ── Context + filter panel ─────────────────────────────────────────────
    ctx_panel_el = _build_context_panel(context, filter_result, sess, sig)

    # ── Open positions ─────────────────────────────────────────────────────
    try:
        open_trades = db_mod.get_open_outcomes()
    except Exception:
        open_trades = []
    pos_panel_el = _build_positions_panel(open_trades)

    # ── News warning ───────────────────────────────────────────────────────
    events = news_mod.get_upcoming_events(hours_ahead=24)
    news_el = ""
    if events:
        news_el = html.Div(className="nqb-no-trade",
                           style={"borderColor": WARN, "color": WARN,
                                  "backgroundColor": "oklch(0.82 0.14 85 / .1)"},
                           children=[
            html.Strong("⚠ High-Impact Events in Next 24H: "),
            ", ".join(f"{e['title']} ({e['date_str']})" for e in events[:3]),
        ])

    fig = _build_chart(df, sig, tf_key)

    return (f"{price:,.2f}", delta_cls, delta_txt, sess_el,
            kpi_items, mtf_panel_el, ctx_panel_el, pos_panel_el,
            fig, news_el, alert_key)


